//! `IndexedMap<K, V>` — a height-versioned `Map<K, V>` plus framework-maintained
//! secondary indexes, so filtered reads are prefix scans of a declared index
//! rather than a `keys()` scan-and-filter.
//!
//! Layout (all ordinary path-prefixed `contract_state` rows, so versioned,
//! reorg-safe, and folded into the checkpoint hash):
//! ```text
//!   <primary>/<key>                        -> V
//!   <primary>#idx/<index_name>/<index_key>/<key> -> ()   (sibling of <primary>)
//! ```
//! `by_index(name, key)` scans `<primary>#idx/<name>/<key>/`, whose child
//! segments ARE the primary keys. A key's index memberships are derived from its
//! value via [`Indexed`] — there is no separate record of them, so the value
//! stays the one source of truth (same as every other generated model). On
//! update the prior entries are recomputed and diffed against the new ones via
//! [`apply_index_diff`]: delete `old − new`, write `new − old`, so an unchanged
//! index (or a no-op re-set) costs zero index ops.
//!
//! Like [`StorageMap`](crate::StorageMap), the type a contract declares is an
//! inert placeholder; the real per-key `get`/`set`/`remove`/`by_index` accessors
//! are generated by the `#[derive(Model)]` field model. The only logic carried
//! here is (a) [`apply_index_diff`], the single audited maintenance routine both
//! the field model and the bulk [`Store`] impl call, and (b) the [`Store`] impl
//! below, which keeps a *wholesale* write of the map (root init, or any nested
//! struct set through `Store`) index-consistent — the field model's per-key path
//! can't catch that.

use alloc::borrow::Cow;
use alloc::format;
use alloc::rc::Rc;
use alloc::string::{String, ToString};
use alloc::vec::Vec;
use core::str::FromStr;

